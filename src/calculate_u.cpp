#include "calculate_u.h"

        Calculate_u::Calculate_u(){}
        Calculate_u::~Calculate_u(){}
        

        double Calculate_u::add(double a, double b)
        {
            return a + b;
        }

        double Calculate_u::subtract(double a, double b)
        {
            return a - b;
        }

        double Calculate_u::average(double a, double b)
        {
            return (a + b) / 2.0;
        }

        double Calculate_u::square(double a)
        {
            return a * a;
        }